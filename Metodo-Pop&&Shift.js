// # O método .pop() nos permite remover o "último" item do array.
 let tasksList = ['Tomar café', 'Reunião', 'Brincar com o cachorro'];
 tasksList.pop();  // remove a último itein
 console.log(tasksList);
// resultado [ 'Tomar café', 'Reunião' ]


// # O método .shift() nos permite remover o "Primeiro" item do array
let List = ['Tomar café', 'Reunião', 'Brincar com o cachorro'];
List.shift();  // remove o primeiro itein
console.log(List);
// resultado  [ 'Reunião', 'Brincar com o cachorro']